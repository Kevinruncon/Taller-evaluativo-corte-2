    private void btnGuardarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarRegistroActionPerformed
        String nombre = txtRegistroNombre.getText().trim();
        String documento = txtRegistroDocumento.getText().trim();
        String telefono = txtRegistroTelefono.getText().trim();
        String correo = txtRegistroCorreo.getText().trim();

        if (nombre == null || nombre.isBlank()
                || documento == null || documento.isBlank()
                || telefono == null || telefono.isBlank() ) {
            JOptionPane.showMessageDialog(this, "Todos los campos deben estar llenos.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        } else if (telefono.length() <= 0) {
            JOptionPane.showMessageDialog(this, "No se pueden agregar datos con valores negativos", "Advertencia", JOptionPane.WARNING_MESSAGE);

        }

        
        DtoHuesped nuevoHues = new DtoHuesped(nombre, documento, telefono, correo);
        boolean confirmacion = HuespedControlador.guardarHuesped(nuevoHues);

        if (confirmacion) {
            JOptionPane.showMessageDialog(this, "El Huesped fue guardada correctamente", "Confirmacion", JOptionPane.INFORMATION_MESSAGE);
            ListarHuesped();
            /*limpiarCampos();*/
        } else {
            JOptionPane.showMessageDialog(this, "El Huesped no se pudo guardar.", "Advertencia", JOptionPane.WARNING_MESSAGE);

        }


    

    }//GEN-LAST:event_btnGuardarRegistroActionPerformed

