    private void ListarHuesped() {
        // Definir siempre los encabezados, aunque no haya datos
        String[] columnas = {"Nombre", "Documento", "Teléfono","Correo"};
        DefaultTableModel modelo = new DefaultTableModel(columnas, 0);

        ArrayList<DtoHuesped> lista = HuespedControlador.getListaHuesped();

        if (lista != null && !lista.isEmpty()) {
            for (DtoHuesped p : lista) {
                Object[] fila = {p.getNombre(), p.getDocumento(), p.getTelefono(),p.getCorreo()};
                modelo.addRow(fila);
            }
        }

        tablaHuesped.setModel(modelo);
    }


