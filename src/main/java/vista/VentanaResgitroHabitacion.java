    public void listarRegistroHabitacion(){
        String[] columnas = {"Numero", "Tipo", "Estado", "Capacidad"};
        DefaultTableModel model = new DefaultTableModel(columnas, 0);
       
        ArrayList<DtoHabitacion> resgitrar = controladorHabitacion.getListaHabitaciones();
        if( resgitrar == null|| resgitrar.isEmpty()){
            JOptionPane.showMessageDialog(this, "NO hay ninguna Habitacion registrada.");
            return;
        }else{
            for (DtoHabitacion h : resgitrar){
                Object[] fila = {
                    h.getNumero(),
                    h.getTipo(),
                    h.getEstado(),
                    h.getCapacidad()
                };
                model.addRow(fila);
            }
            tablaHabitacion.setModel(model);
        }
        ventanaPrincipal.getVentanaRegistro().listarHabitacionesDisponibles();

    }

