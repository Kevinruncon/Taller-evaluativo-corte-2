    private void btnGuardarHabitacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarHabitacionActionPerformed
    int numero = Integer.parseInt(txtNumeroHabitacion.getText());
    String tipo = comboboxTipoHabitacion.getSelectedItem().toString();
    int capacidad = Integer.parseInt(txtCapacidadHabitacion.getText());
    String estado = txtEstadoHabitacion.getText();
    
    if(numero <= 0 || tipo == null || tipo.isBlank() || capacidad <=0 || estado == null || estado.isBlank()){
    JOptionPane.showMessageDialog(this, "Porfavor llenar todos los campos de manera correcta.");
    return;
    }
    DtoHabitacion hab = null;
try {
    hab = controladorHabitacion.buscarHabitacion(numero);
} catch (HabitacionException e) {
}

if (hab != null) {
    JOptionPane.showMessageDialog(this, "No es posible registrar la habitación, ya existe.");
    return;
}

    DtoHabitacion nuevaHab = new  DtoHabitacion(numero, tipo, capacidad, estado);
    boolean guardar = controladorHabitacion.guardarHabitacion(nuevaHab);
    if(guardar){
        JOptionPane.showMessageDialog(this, "Habitacion guardada correctamente.");
        listarRegistroHabitacion();
    }else{
        JOptionPane.showMessageDialog(this, "La habitacion no se pudo guardar");
    }
    

    }//GEN-LAST:event_btnGuardarHabitacionActionPerformed

